def get_all_polyphonic_chars():
    """获取所有多音字的去重列表"""
    all_chars = {}

    for char, pinyin in HIGH_FREQUENCY + MEDIUM_FREQUENCY + ANALYZED + CLASSICAL_CHINESE:
        if char not in all_chars:
            all_chars[char] = pinyin

    return all_chars

def get_pending_chars():
    """获取待分析的多音字（排除已完成的）"""
    analyzed_chars = {char for char, _ in ANALYZED}
    pending = {}

    for char, pinyin in HIGH_FREQUENCY + MEDIUM_FREQUENCY + CLASSICAL_CHINESE:
        if char not in analyzed_chars:
            pending[char] = pinyin

    return pending

if __name__ == '__main__':
    all_chars = get_all_polyphonic_chars()
    pending = get_pending_chars()

    print(f"总计多音字: {len(all_chars)}个")
    print(f"已完成分析: {len(ANALYZED)}个")
    print(f"待分析: {len(pending)}个")
    print(f"\n待分析的多音字列表:")
    for char, pinyin in sorted(pending.items()):
        print(f"  {char}: {', '.join(pinyin)}")
