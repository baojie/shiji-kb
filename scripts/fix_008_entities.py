#!/usr/bin/env python3
"""
008_高祖本纪 实体标注深度反思修正脚本
执行以下修正：
1. 旧格式转换：⟦◈⟧→〖[〗, ⟦◉⟧→〖[〗
2. 刑法动词补标
3. 主人公省称补标
4. 群体称谓补标
5. 其他遗漏实体补标
"""

import re
import sys

def apply_fixes(content):
    """应用所有修正"""
    fixes_count = {
        '旧格式转换': 0,
        '刑法动词补标': 0,
        '人名省称补标': 0,
        '群体称谓补标': 0,
        '其他修正': 0
    }

    # 1. 旧格式转换 ⟦◈...⟧ → 〖[...〗
    # 需保留消歧说明（如 攻取|投降 等）
    pattern1 = r'⟦◈([^⟧]+)⟧'
    matches1 = re.findall(pattern1, content)
    content = re.sub(pattern1, r'〖[\1〗', content)
    fixes_count['旧格式转换'] += len(matches1)

    # 2. 旧格式转换 ⟦◉...⟧ → 〖[...〗
    pattern2 = r'⟦◉([^⟧]+)⟧'
    matches2 = re.findall(pattern2, content)
    content = re.sub(pattern2, r'〖[\1〗', content)
    fixes_count['旧格式转换'] += len(matches2)

    # 3. 刑法动词补标（严格语境匹配，避免误标）
    verb_fixes = [
        # 杀 - 在"人杀"/"杀吾"/"杀之"/"杀其"等语境
        (r'([人将])杀([吾其之])', r'\1〖[杀〗\2', '杀'),
        (r'由所杀蛇', r'由所〖[杀〗蛇', '杀'),
        (r'杀者〖\?', r'〖[杀〗者〖?', '杀'),
        (r'皆多杀其', r'皆多〖[杀〗其', '杀'),
        (r'，杀之', r'，〖[杀〗之', '杀'),
        (r'又杀之', r'又〖[杀〗之', '杀'),

        # 诛 - 在"诛令"/"诛诸"等语境
        (r'共诛令', r'共〖[诛〗令', '诛'),
        (r'欲诛无道', r'欲〖[诛〗无道', '诛'),
        (r'诛残贼', r'〖[诛〗残贼', '诛'),
        (r'诸将皆诛', r'诸将皆〖[诛〗', '诛'),

        # 斩 - 在"击斩"/"斩之"等语境
        (r'击斩蛇', r'击〖[斩〗蛇', '斩'),
        (r'斩之，故', r'〖[斩〗之，故', '斩'),
        (r'斩以徇', r'〖[斩〗以徇', '斩'),
        (r'出骂者斩之', r'出骂者〖[斩〗之', '斩'),

        # 屠 - 在"屠城"/"俱屠"等语境
        (r'，屠〖=', r'，〖[屠〗〖=', '屠'),
        (r'今屠〖=', r'今〖[屠〗〖=', '屠'),
        (r'〖#父子〗俱屠', r'〖#父子〗俱〖[屠〗', '屠'),
        (r'行屠〖=', r'行〖[屠〗〖=', '屠'),

        # 破 - 在"破之"/"破二军"/"大破之"等语境（已标注⟦◈破⟧的不处理）
        (r'出与战，破之', r'出与战，〖[破〗之', '破'),
        (r'，破二军', r'，〖[破〗二军', '破'),
        (r'大破之([。，〖])', r'大〖[破〗之\1', '破'),
        (r'，因大破之', r'，因大〖[破〗之', '破'),
        (r'又战其北，大破之', r'又〖[战〗其北，大〖[破〗之', '破'),
        (r'乘胜，遂破之', r'乘胜，遂〖[破〗之', '破'),

        # 战 - 在"与战"/"未战"等语境（已标注⟦◈战⟧的不处理）
        (r'，未战', r'，未〖[战〗', '战'),
        (r'出与战', r'出与〖[战〗', '战'),
        (r'与〖\'秦〗军战於', r'与〖\'秦〗军〖[战〗於', '战'),
        (r'与〖\'秦〗军战，', r'与〖\'秦〗军〖[战〗，', '战'),
        (r'军战，破之', r'军〖[战〗，破之', '战'),
        (r'又战其北', r'又〖[战〗其北', '战'),
        (r'战必胜', r'〖[战〗必胜', '战'),
        (r'挑战([。，！])', r'挑〖[战〗\1', '战'),
        (r'苦战数岁', r'苦〖[战〗数岁', '战'),
        (r'慎勿与战', r'慎勿与〖[战〗', '战'),

        # 击 - 在特定语境（去掉已标注⟦◈击⟧的）
        (r'([从击])之，[^〖]', r'\1之，〖[击〗', '击'),

        # 降 - 在特定语境
        (r'人已服降', r'人已服〖[降〗', '降'),
        (r'杀已降', r'〖[杀〗已〖[降〗', '降'),
        (r'约降，封', r'约〖[降〗，封', '降'),

        # 围 - 在"围之"等语境（去掉已标注的）
        (r'围之〖=', r'〖[围〗之〖=', '围'),

        # 灭 - 在特定语境
        (r'无不残灭', r'无不残〖[灭〗', '灭'),

        # 族 - 在"族其家"/"种族"等语境
        (r'种族其家', r'种〖[族〗其家', '族'),

        # 拔 - 在特定语境（去掉已标注的）
        (r'，拔之。还', r'，〖[拔〗之。还', '拔'),
        (r'未拔([。，])', r'未〖[拔〗\1', '拔'),

        # 败 - 在特定语境
        (r'皆败([。，])', r'皆〖[败〗\1', '败'),
        (r'兵败，还', r'兵〖[败〗，还', '败'),
        (r'，又复败', r'，又复〖[败〗', '败'),
        (r'壹败涂地', r'壹〖[败〗涂地', '败'),

        # 定 - 在"北定"/"略定"/"已定"等语境
        (r'将兵北定〖\'', r'将兵北〖[定〗〖\'', '定'),
        (r'而遣诸将略定', r'而遣诸将略〖[定〗', '定'),
        (r'已定〖=', r'已〖[定〗〖=', '定'),
        (r'遂略定〖\'', r'遂略〖[定〗〖\'', '定'),
        (r'止定〖\'', r'止〖[定〗〖\'', '定'),
        (r'定〖\'燕〗、〖\'代〗', r'〖[定〗〖\'燕〗、〖\'代〗', '定'),

        # 下 - 在"下之"/"未下"等语境
        (r'，下之', r'，〖[下〗之', '下'),
        (r'未下([。，])', r'未〖[下〗\1', '下'),
        (r'遂下〖\'', r'遂〖[下〗〖\'', '下'),
        (r'遂复下〖\'', r'遂复〖[下〗〖\'', '下'),

        # 反 - 在特定语境
        (r'([告谋欲因])反([。，〖])', r'\1〖[反〗\2', '反'),
        (r'([而复])反〖\'', r'\1〖[反〗〖\'', '反'),

        # 叛 - 在特定语境
        (r'([与倍])叛之', r'\1〖[叛〗之', '叛'),
        (r'群臣稍倍叛之', r'群臣稍倍〖[叛〗之', '叛'),
        (r'叛〖\'楚〗', r'〖[叛〗〖\'楚〗', '叛'),
        (r'举兵叛〖\'', r'举兵〖[叛〗〖\'', '叛'),
        (r'〖#大臣〗内叛', r'〖#大臣〗内〖[叛〗', '叛'),
        (r'〖#诸侯〗外反', r'〖#诸侯〗外〖[反〗', '反'),

        # 虏 - 在特定语境
        (r'，而虏〖', r'，而〖[虏〗〖', '虏'),
        (r'掠卤，〖\'', r'掠〖[卤〗，〖\'', '卤'),
        (r'劫掠〖\'', r'劫〖[掠〗〖\'', '掠'),
        (r'所劫掠者', r'所劫〖[掠〗者', '掠'),

        # 走 - 在逃跑语境
        (r'败，还走', r'〖[败〗，还〖[走〗', '走'),
        (r'走，令', r'〖[走〗，令', '走'),
        (r'走〖=', r'〖[走〗〖=', '走'),
        (r'走入〖', r'〖[走〗入〖', '走'),
        (r'走至〖=', r'〖[走〗至〖=', '走'),
        (r'已破走〖@', r'已〖[破〗〖[走〗〖@', '走'),
        (r'败走〖=', r'〖[败〗〖[走〗〖=', '走'),

        # 阬 - 在特定语境
        (r'皆阬之', r'皆〖[阬〗之', '阬'),
        (r'诈阬〖\'', r'诈〖[阬〗〖\'', '阬'),

        # 奔/逃
        (r'([东南])走([。，〖])', r'\1〖[走〗\2', '走'),

        # 追 - 在特定语境
        (r'，追得', r'，〖[追〗得', '追'),
        (r'独追北', r'独〖[追〗北', '追'),
        (r'进兵追〖@', r'进兵〖[追〗〖@', '追'),

        # 距 - 在抵抗语境
        (r'兵以自益，距之', r'兵以自益，〖[距〗之', '距'),

        # 赦
        (r'〖\[大赦', r'〖[大赦〗', '大赦'),
        (r'赦〖=', r'〖[赦〗〖=', '赦'),
        (r'赦〖\'', r'〖[赦〗〖\'', '赦'),
        (r'皆赦之', r'皆〖[赦〗之', '赦'),

        # 弃市
        (r'弃巿([。，])', r'〖[弃市〗\1', '弃市'),
    ]

    for pattern, replacement, verb in verb_fixes:
        count = len(re.findall(pattern, content))
        if count > 0:
            content = re.sub(pattern, replacement, content)
            fixes_count['刑法动词补标'] += count
            print(f"  {verb}: {count}处")

    # 4. 人名省称补标 - 高祖/邦/沛公等主人公的重复出现
    # 使用精确匹配，避免误标
    name_fixes = [
        # 高祖 - 精确匹配
        (r'([，。；])〖@高祖〗([，。曰为])', r'\1〖@高祖〗\2'),  # 已标注的跳过
    ]

    # 这部分需要更细致的人工审查，暂不自动处理

    # 5. 群体称谓补标
    group_fixes = [
        (r'([〖#])父老([〗])', r'\1父老\2'),  # 已标注的跳过
        (r'诸〖#父老〗', r'诸〖#父老〗'),  # 已标注的保持
        # 新增标注
        (r'([，。])父老([曰皆])', r'\1〖#父老〗\2', '父老'),
        (r'([与])父老([约])', r'\1〖#父老〗\2', '父老'),
        (r'问〖#父老', r'问〖#父老〗', '父老'),

        (r'([，。为])子弟([〖，。])', r'\1〖#子弟〗\2', '子弟'),
        (r'诸〖#子弟', r'诸〖#子弟〗', '子弟'),

        (r'([。，])诸将([或皆与及])', r'\1〖#诸将〗\2', '诸将'),
        (r'([属])诸将([〖])', r'\1〖#诸将〗\2', '诸将'),

        (r'([，。])百姓([，。〖])', r'\1〖#百姓〗\2', '百姓'),
        (r'抚〖#百姓', r'抚〖#百姓〗', '百姓'),

        (r'([，。])群臣([皆稍])', r'\1〖#群臣〗\2', '群臣'),
        (r'〖#诸侯〗群臣', r'〖#诸侯〗〖#群臣〗', '群臣'),

        (r'([。，])士卒([多皆])', r'\1〖#士卒〗\2', '士卒'),
        (r'及〖#士卒', r'及〖#士卒〗', '士卒'),

        (r'([，。])丁壮([苦])', r'\1〖#丁壮〗\2', '丁壮'),

        (r'([，。])老弱([罢])', r'\1〖#老弱〗\2', '老弱'),

        (r'([，。])诸吏([人])', r'\1〖#诸吏〗\2', '诸吏'),
    ]

    for pattern, replacement, *group_type in group_fixes:
        if len(group_type) > 0:
            count = len(re.findall(pattern, content))
            if count > 0:
                content = re.sub(pattern, replacement, content)
                fixes_count['群体称谓补标'] += count
                print(f"  {group_type[0]}: {count}处")

    return content, fixes_count

def main():
    file_path = '/home/baojie/work/shiji-kb/chapter_md/008_高祖本纪.tagged.md'

    print("读取文件...")
    with open(file_path, 'r', encoding='utf-8') as f:
        content = f.read()

    print("应用修正...")
    new_content, fixes_count = apply_fixes(content)

    print("\n修正统计：")
    total = 0
    for category, count in fixes_count.items():
        print(f"  {category}: {count}处")
        total += count
    print(f"  总计: {total}处")

    print(f"\n写入修正后的文件...")
    with open(file_path, 'w', encoding='utf-8') as f:
        f.write(new_content)

    print("完成！")
    return total

if __name__ == '__main__':
    total = main()
    sys.exit(0 if total > 0 else 1)
