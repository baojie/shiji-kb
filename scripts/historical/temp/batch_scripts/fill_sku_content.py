def fill_eureka_file(chapter_num):
    """填充Eureka文件"""
    chapter_dir = CHAPTERS_DIR / f"chapter_{chapter_num:03d}"
    eureka_file = chapter_dir / "eureka.md"

    if chapter_num not in CHAPTER_CONTENT:
        print(f"  跳过章节{chapter_num}（无内容定义）")
        return

    data = CHAPTER_CONTENT[chapter_num]
    title = data["title"]
    eurekas = data["eureka"]

    eureka_blocks = []
    for i, eureka in enumerate(eurekas, 1):
        # 分离标题和内容
        parts = eureka.split("——", 1)
        if len(parts) == 2:
            eureka_title, eureka_content = parts
            eureka_title = eureka_title.split("：")[1] if "：" in eureka_title else eureka_title
        else:
            eureka_title = f"洞察{i}"
            eureka_content = eureka

        block = f"## {eureka_title}\n\n{eureka_content.strip()}"
        eureka_blocks.append(block)

    content = f"""# 灵感笔记 — {title}

知识提取过程中发现的跨领域洞察和创意。

---

{chr(10).join(eureka_blocks)}

---

**洞察统计**：{len(eurekas)}条
**来源章节**：{title}
**主题分布**：军事策略、政治智慧、人性洞察、历史规律
**版本**：v1.0
**创建日期**：2026-04-05
"""

    eureka_file.write_text(content, encoding='utf-8')
    print(f"  ✓ 填充 eureka.md（{len(eurekas)}条洞察）")

def main():
    """主函数"""
    print("开始填充SKU核心内容...")

    # 优先处理重点章节
    priority_chapters = [92, 101, 105, 109, 110]

    for chapter_num in priority_chapters:
        if chapter_num in CHAPTER_CONTENT:
            print(f"\n处理 {chapter_num:03d}_{CHAPTER_CONTENT[chapter_num]['title']}...")
            fill_eureka_file(chapter_num)

    print("\n✅ 核心章节SKU内容填充完成！")
    print("\n已完成Eureka填充的章节：")
    for ch in priority_chapters:
        if ch in CHAPTER_CONTENT:
            print(f"  - {ch:03d} {CHAPTER_CONTENT[ch]['title']}")

if __name__ == "__main__":
    main()
