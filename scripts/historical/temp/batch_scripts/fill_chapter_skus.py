def fill_chapter_content(chapter_num: str):
    """填充单个章节的内容"""
    if chapter_num not in CHAPTER_CONTENTS:
        return

    content = CHAPTER_CONTENTS[chapter_num]
    chapter_dir = OUTPUT_DIR / f"chapter_{chapter_num}"

    # 填充facts
    for fact_id, fact_data in content.get("facts", {}).items():
        if fact_data["type"] == "json":
            fact_file = chapter_dir / "skus" / "facts" / f"{fact_id}.json"
            fact_content = {
                "name": fact_data["name"],
                "description": fact_data["description"],
                "data": fact_data["content"]
            }
            fact_file.write_text(
                json.dumps(fact_content, ensure_ascii=False, indent=2),
                encoding="utf-8"
            )
        else:  # markdown
            fact_file = chapter_dir / "skus" / "facts" / f"{fact_id}.md"
            fact_file.write_text(fact_data["content"], encoding="utf-8")

    # 填充skills
    for skill_id, skill_data in content.get("skills", {}).items():
        skill_file = chapter_dir / "skus" / "skills" / f"{skill_id}.md"
        skill_file.write_text(skill_data["content"], encoding="utf-8")

    # 更新eureka.md
    if "eureka" in content:
        eureka_file = chapter_dir / "eureka.md"
        eureka_content = f"""# 灵感笔记 — {content['title']}

知识提取过程中发现的跨领域洞察和创意。

---

"""
        for i, insight in enumerate(content["eureka"], 1):
            eureka_content += f"## 洞察{i}\n\n{insight}\n\n"

        eureka_content += f"""---

**洞察统计**：{len(content['eureka'])}条
**来源章节**：{content['title']}
**版本**：v1.0
**创建日期**：2026-04-05
"""
        eureka_file.write_text(eureka_content, encoding="utf-8")

    print(f"✓ 章节 {chapter_num} ({content['title']}) 内容填充完成")


def main():
    """主函数"""
    print("开始填充章节SKU内容...")
    print()

    for chapter_num in CHAPTER_CONTENTS.keys():
        fill_chapter_content(chapter_num)

    print()
    print("=" * 60)
    print("内容填充完成")
    print("=" * 60)
    print()
    print("已填充章节：")
    for chapter_num, content in CHAPTER_CONTENTS.items():
        facts_count = len(content.get("facts", {}))
        skills_count = len(content.get("skills", {}))
        eureka_count = len(content.get("eureka", []))
        print(f"  {chapter_num} {content['title']}: {facts_count} facts, {skills_count} skills, {eureka_count} eureka")


if __name__ == "__main__":
    main()
