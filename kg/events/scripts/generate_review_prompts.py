def get_era_framework(chapter_id, chapter_name):
    """根据章节获取时代框架文本"""
    era = CHAPTER_ERA.get(chapter_id, "")

    # 直接匹配
    if era in ERA_FRAMEWORKS:
        return ERA_FRAMEWORKS[era]

    # 通论类
    if era.startswith("通论"):
        return TONGLUN_FRAMEWORK.format(chapter_name=chapter_name)

    # 春秋战国类（世家、列传）
    if "春秋" in era or "战国" in era:
        # 推断涉及的国家
        state_map = {
            "吴": "吴", "齐": "齐", "鲁": "鲁", "燕": "燕",
            "卫": "卫", "宋": "宋", "晋": "晋", "楚": "楚",
            "越": "越", "郑": "郑", "赵": "赵", "魏": "魏",
            "韩": "韩", "田齐": "田齐", "秦": "秦",
        }
        states = "、".join(s for key, s in state_map.items() if key in chapter_name or key in era)
        if not states:
            states = "相关诸侯国"
        return CHUNQIU_FRAMEWORK.format(era_label=era, states=states)

    # 西汉类
    if "西汉" in era:
        for key, val in ERA_FRAMEWORKS.items():
            if key == era:
                return val
        return ERA_FRAMEWORKS.get("西汉", f"本章涉及{era}时期。参照 `reign_periods.json` 中相关数据。")

    # 兜底
    return f"本章涉及{era}时期。参照 `reign_periods.json` 和 `中国历史大事年表.md` 中的相关数据。"


