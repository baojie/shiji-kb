def get_chapter_list():
    """从文件系统读取所有章节"""
    chapters = []
    for path in sorted(glob.glob(os.path.join(EVENTS_DIR, "*_事件索引.md"))):
        fname = os.path.basename(path).replace("_事件索引.md", "")
        parts = fname.split("_", 1)
        if len(parts) == 2:
            chapters.append((parts[0], parts[1]))
    return chapters


def parse_args(args):
    """解析命令行参数，返回要处理的章节ID集合"""
    if not args:
        return None  # 全部

    ids = set()
    for arg in args:
        if "-" in arg and not arg.startswith("-"):
            start, end = arg.split("-", 1)
            for i in range(int(start), int(end) + 1):
                ids.add(f"{i:03d}")
        else:
            ids.add(arg.zfill(3))
    return ids


def main():
    target_ids = parse_args(sys.argv[1:])
    chapters = get_chapter_list()

    os.makedirs(OUTPUT_DIR, exist_ok=True)

    count = 0
    for chapter_id, chapter_name in chapters:
        if target_ids is not None and chapter_id not in target_ids:
            continue

        prompt = generate_prompt(chapter_id, chapter_name)
        outpath = os.path.join(OUTPUT_DIR, f"review_{chapter_id}.md")
        with open(outpath, "w", encoding="utf-8") as f:
            f.write(prompt)
        count += 1

    print(f"已生成 {count} 个审查提示词 → {OUTPUT_DIR}/")

    if target_ids:
        for cid in sorted(target_ids):
            print(f"  review_{cid}.md")
    else:
        print(f"  review_001.md ~ review_130.md")


if __name__ == "__main__":
    main()
