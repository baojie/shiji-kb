# ─── 修改 TTL ─────────────────────────────────────────────────

RE_CLASS = re.compile(
    r'^(per:(\S+)\s+a\s+owl:Class\s*;.*?rdfs:label\s+"([^"]+)"@zh)\s*(?:;\s*:order\s+\d+\s*)?\.',
    re.MULTILINE,
)

def add_order(m):
    body = m.group(1)
    label = m.group(3)
    order = ORDER.get(label, 99)
    return f'{body} ; :order {order} .'

text = TTL.read_text(encoding='utf-8')
new_text, n = RE_CLASS.subn(add_order, text)
TTL.write_text(new_text, encoding='utf-8')
print(f'✓ 已为 {n} 个 owl:Class 写入 :order 属性')

# 验证
